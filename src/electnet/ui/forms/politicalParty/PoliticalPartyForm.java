    private void idFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idFieldActionPerformed
        FileManager fileManager = new FileManager();
        String id = idField.getText();
        String politicalParty = null;
        try {
            politicalParty = fileManager.findDataById(pathname, id);
            String[] politicalPartyData = fileManager.splitString(politicalParty, ";");
            foundPoliticalParty = true;
            modeField.setText("Modificando");
            descriptionField.setText(politicalPartyData[1]);
            voteField.setText(politicalPartyData[2]);
            oldPoliticalParty = politicalParty;
        } catch (IOException e) {
            System.out.print("Hola");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_idFieldActionPerformed

