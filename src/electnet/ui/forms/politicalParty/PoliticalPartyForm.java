    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FileManager fileManager = new FileManager();

        String id = idField.getText();
        String description = descriptionField.getText();
        String vote = voteField.getText();

        String delimiter = ";"; // Delimitador

        newPoliticalParty = String.join(delimiter, id, description, vote);
        if (id.isEmpty()) {
            messageField.setText("Proporcione un ID para el Partido");
            return;
        } else {
            messageField.setText("");
        }
        try {
            if (foundPoliticalParty) {
                System.out.print("Modo Ediccion");
                System.out.println(oldPoliticalParty);
                System.out.println(newPoliticalParty);
                fileManager.editFile(pathname, oldPoliticalParty, newPoliticalParty);
            } else {
                fileManager.writeToFile(pathname, newPoliticalParty);
            }
        } catch (IOException e) {

        }


    }//GEN-LAST:event_jButton1ActionPerformed

