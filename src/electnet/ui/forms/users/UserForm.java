    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            FileManager fileManager = new FileManager();

            // Recoger los valores de los campos de texto
            String username = userNameField.getText();
            String password = new String(passwordField.getPassword()); // Deber√≠as encriptar la contrase√±a antes de guardarla
            String role = roleField.getText();
            String firstname = firstNameField.getText();
            String lastname = lastNameField.getText();
            String email = emailField.getText();

            String delimiter = ";"; // Delimitador

            newUser = String.join(delimiter, username, password, role, firstname, lastname, email);

            if (username.isEmpty()) {
                messageField.setText("Proporcione un nombre de usuario");
                return;
            } else {
                messageField.setText("");
            }
            if (foundUser) {
                System.out.print("Modo Ediccion");
                System.out.println(oldUser);
                System.out.println(newUser);
                fileManager.editFile(pathname, oldUser, newUser);
            } else {
                fileManager.writeToFile("database/users.txt", newUser);
            }

        } catch (IOException e) {
            System.out.print(e);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

