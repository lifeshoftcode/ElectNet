    private void userNameFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userNameFieldActionPerformed
        FileManager fileManager = new FileManager();
        String userName = userNameField.getText();
        String user = null;

        try {
            user = fileManager.findDataById("database/users.txt", userName);
            String[] userData = fileManager.splitString(user, ";");
            foundUser = true;
            modeField.setText("Modificando");
            passwordField.setText(userData[1]);
            roleField.setText(userData[2]);
            firstNameField.setText(userData[3]);
            lastNameField.setText(userData[4]);
            emailField.setText(userData[5]);
            oldUser = user;
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error: No se pudo encontrar la informaci√≥n del usuario.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_userNameFieldActionPerformed

