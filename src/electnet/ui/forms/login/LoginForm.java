    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        AuthController authController = new AuthController();
        String username = userNameField.getText();
        String password = passwordField.getText();
        boolean success = false;
        try {
            success = authController.signInUser(username, password);
            if (success) {
                System.out.print("Logueado");
                Routes.ShowLogin(false);
                WindowManager.hideWindow(new LoginForm());
                WindowManager.showWindow(new MainMenu());
                 messageField.setText("");
            } else {
                messageField.setText("Error al Iniciar Sesión, Comprueba los datos ingresados.");
              
            }
        } catch (IOException e) {

        }

    }//GEN-LAST:event_jButton1ActionPerformed

